    public String[][] getMatrizParejas() {
        String[][] matrizNombreParejas = new String[5][2];
        System.out.println("");
        System.out.println("Matriz Vista Parejas:");
        for (int i = 0; i < matrizNombreParejas.length; i++) {
            int k = 0;
            for (int j = 0; j < matrizNombreParejas[i].length; j++) {
                matrizNombreParejas[i][j] = matrizParejas[i][k];
                k = k + 4;
                System.out.print(matrizNombreParejas[i][j] + ",");
            }
            System.out.println("");
        }
        // le pasamos a la clase vistaProblemas la matriz de parejas 
        //viewProblemas.setMatrizNombreParejas(matrizNombreParejas);                
        return matrizNombreParejas;
    }

